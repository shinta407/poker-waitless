/**
 * Generates a focus ring style string for use in Tailwind classes or inline styles
 */
export function getFocusRingStyle(): string {
  return `outline: ${focusRing.width} ${focusRing.style} ${focusRing.color}; outline-offset: ${focusRing.offset};`
}

/**
 * Generates WCAG-compliant touch target size
 */
export function getTouchTargetSize(size: keyof typeof touchTargets = 'comfortable'): string {
  return `${touchTargets[size]}px`
}
